function areaCuadrado(lado1, lado2) {
    areaCuadrado = lado1 * lado2;
    console.log(`El area de un cuadrado con medidas de ${lado1} mt y ${lado2} mt es igual a ${areaCuadrado} mt^2 `);
}
areaCuadrado(10, 15);


